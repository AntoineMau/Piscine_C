#include "../../include/ft.h"

void	rush00(int x, int y, char *str)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	k = 0;
	while (++i <= y && !(x <= 0 || y <= 0))
	{
		j = 0;
		while (++j <= x)
		{
			if ((j == 1 && i == 1) || (j == x && i == y) || (j == x && i == 1)
					|| (j == 1 && i == y))
				str[k] = 'o';
			else if (i == 1 || i == y)
				str[k] = '-';
			else if (j == 1 || j == x)
				str[k] = '|';
			else
				str[k] = ' ';
			k++;
		}
		str[k++] = '\n';
	}
	str[--k] = '\0';
}
