#include "../include/ft_header.h"

int		ft_addition(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int		ft_soustraction(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int		ft_multiplication(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int		ft_division(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int		ft_modulo(int nb1, int nb2)
{
	return (nb1 % nb2);
}
