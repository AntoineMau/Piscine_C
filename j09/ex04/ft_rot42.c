char	*ft_rot42(char *str)
{
	int		i;
	int		j;

	i = 0;
	while (str[i])
	{
		j = 0;
		while (j < 16)
		{
			if ((str[i] >= 'a' && str[i] <= 'z')
				|| (str[i] >= 'A' && str[i] <= 'Z'))
			{
				if (str[i] == 'Z' || str[i] == 'z')
					str[i] -= 25;
				else
					str[i]++;
			}
			j++;
		}
		i++;
	}
	return (str);
}
