int		ft_count_x(char *str)
{
	int		i;
	int		count_x;

	i = 0;
	while (str[i] != '\n' && str[i] != '\0')
		i++;
	count_x = 0;
	i++;
	while (str[i] != '\n' && str[i] != '\0')
	{
		i++;
		count_x++;
	}
	return (count_x);
}
